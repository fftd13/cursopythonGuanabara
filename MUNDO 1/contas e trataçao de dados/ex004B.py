x=input('digite aqui:')
print('este objeto é numerico?',x.isnumeric())
print('este objeto é alfabetico?',x.isalpha())
print('este objeto é alphanumerico?',x.isalnum())
print('este objeto esta em maiusculo?',x.isupper())
print('este objeto esta em minusculo?',x.islower())
print('este objeto é decimal?',x.isdecimal())
print(type(x))


